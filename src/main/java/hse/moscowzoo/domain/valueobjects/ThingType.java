package hse.moscowzoo.domain.valueobjects;

/**
 * Перечисление типов предметов в зоопарке.
 * Используется для классификации различных предметов инвентаря.
 *
 * @author mortun5391
 * @version 1.0
 * @since 2025
 */
public enum ThingType {
    /** Компьютер - электронное оборудование */
    COMPUTER,
    /** Стол - мебель */
    TABLE,
    /** Клетка - сооружение для содержания животных */
    CAGE,
    /** Миска для кормления - посуда для животных */
    FEEDING_BOWL
}